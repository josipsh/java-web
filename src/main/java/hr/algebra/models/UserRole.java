package hr.algebra.models;

public class UserRole {
    private int id;
    private String name;

    public UserRole() {
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
